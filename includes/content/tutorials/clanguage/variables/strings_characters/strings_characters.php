  <h2>Tutorial</h2>
  <p>Two other variable types that C allows, are strings and characters. Strings are actually an array of characters, but they aren’t initialized like an array. We can initialize strings by doing the following:</p>

  <img class="tut" src="http://web-students.armstrong.edu/~jm4386/includes/images/tutorials/clanguage/variables/strings_characters/screenshots/string_variable.png" alt="String variable">
  <p>Figure 1: String variable.</p>

  <p>In this example the variable ‘x’ is a string with the value, “This is a string.” Strings can be added to other strings and even integers. If an integer is added to a string, the integer value is turned into a string value and appended immediately at the end of the string.</p>

  <p>Characters, denoted as char, are essentially a piece of a string; they are one single ASCII character. We can initialize characters by doing the following:</p>

  <img class="tut" src="http://web-students.armstrong.edu/~jm4386/includes/images/tutorials/clanguage/variables/strings_characters/screenshots/character_variable.png" alt="Character variable">
  <p>Figure 2: Character variable.</p>

  <p>Notice that in this example we are using single quotes instead of the double quotes we used to initialize the string -- this is necessary for the compiler to understand that this is a character. In this example, the variable ‘x’ is a character with the value ‘a’. It’s important to note that characters also have integer values, and they can be treated as though they are integers -- this will become important in later sections.</p>

  <h2>Summary</h2>
  <p>String anc character variables are useful for storing text data to be used later.</p>

